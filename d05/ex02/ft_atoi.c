int		ft_atoi(char *str)
{
	int i;
	int mod;
	int total;

	i = 0;
	mod = 1;
	total = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
		mod = str[i++] == '+' ? 1 : -1;
	while (str[i] >= '0' && str[i] <= '9')
		total = total * 10 + (str[i++] - '0');
	return (total * mod);
}
