int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (!*s1 || !*s2)
		return (*s1 - *s2);
	while (n-- && *s1++ == *s2++ && !(*s1 == '\0' && *s2 == '\0'))
		;
	return (*(s1 - 1) - *(s2 - 1));
}
