int		ft_is_prime(int nb)
{
	long n;
	int w;

	if (nb == 2 || nb == 3)
		return (1);
	if (!(nb % 2) || !(nb % 3))
		return (0);
	n = 5;
	w = 2;
	while (n * n <= nb)
	{
		if (!(nb % n))
			return (0);
		n += w;
		w = 6 - w;
	}
	return (1);
}
