int		ft_is_prime(int nb)
{
	long	n;
	int		a;

	if (nb == 2 || nb == 3)
		return (1);
	if (nb <= 1 || !(nb % 2) || !(nb % 3))
		return (0);
	n = 5;
	a = 2;
	while (n * n <= nb)
	{
		if (!(nb % n))
			return (0);
		n += a;
		a = 6 - a;
	}
	return (1);
}
